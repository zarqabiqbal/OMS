		<section>Ciprofloxacin is a fluoroquinolone (flor-o-KWIN-o-lone) antibiotic that fights bacteria in the body.
Ciprofloxacin is used to treat different types of bacterial infections. It is also used to treat people who have been exposed to anthrax or certain types of plague.
Fluoroquinolone antibiotics can cause serious or disabling side effects. Ciprofloxacin should be used only for infections that cannot be treated with a safer antibiotic</section>
