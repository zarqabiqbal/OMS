<article id="art5">
	<Header>5. BENADRYL</Header>
	<section>

Benadryl (diphenhydramine) is an antihistamine that reduces the effects of natural chemical histamine in the body. Histamine can produce symptoms of sneezing, itching, watery eyes, and runny nose.
Benadryl is used to treat sneezing, runny nose, watery eyes, hives, skin rash, itching, and other cold or allergy symptoms.
Benadryl is also used to treat motion sickness, to induce sleep, and to treat certain symptoms of Parkinson's disease</section>
